export function WidgetHelp({
  widget,
  open,
  onClose,
}: {
  widget: DashboardWidgetRead;
  open: boolean;
  onClose: () => void;
}) {
  const meta = HELP[widget.widget_type] ?? {
    title: "Виджет",
    body: "Описание пока не задано.",
  };
  return (
    <Modal
      open={open}
      onCancel={onClose}
      onOk={onClose}
      title={meta.title}
      footer={null}
      destroyOnHidden
    >
      <Typography.Paragraph>{meta.body}</Typography.Paragraph>
      <Typography.Title level={5} style={{ marginTop: 16 }}>
        Текущие настройки
      </Typography.Title>
      <ul style={{ paddingLeft: 20, margin: 0 }}>
        <li>
          <Typography.Text>Тип: </Typography.Text>
          <Typography.Text code>{widget.widget_type}</Typography.Text>
        </li>
        <li>
          <Typography.Text>Источник данных: </Typography.Text>
          <Typography.Text code>{widget.datasource_code ?? "—"}</Typography.Text>
        </li>
        {widget.datasource_params && (
          <li>
            <Typography.Text>Параметры: </Typography.Text>
            <Typography.Text code>
              {JSON.stringify(widget.datasource_params)}
            </Typography.Text>
          </li>
        )}
      </ul>
    </Modal>
  );
}
